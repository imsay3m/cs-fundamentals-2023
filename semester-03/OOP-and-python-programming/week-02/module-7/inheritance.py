# inheritance provides you "is a" relation


class Animal:
    pass


# Dog is an animal
class Dog(Animal):
    pass


# Tiger is an animal
class Tiger(Animal):
    pass


class Furniture:
    pass


# Chair is a Furniture
class Chair(Furniture):
    pass


# Table is a Furniture
class Table(Furniture):
    pass


# Bed is a Furniture
class Bed(Furniture):
    pass
